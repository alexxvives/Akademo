#!/usr/bin/env node
/**
 * generate-links-sql.js
 *
 * Reads links.csv (exported from docs/onboarding/{client}/queries/06_urls.sql)
 * and generates import-links.sql with INSERT OR IGNORE statements for LessonLink.
 *
 * Usage:
 *   1. Export 06_urls.sql from phpMyAdmin → save as links.csv in project root
 *   2. Set ACADEMY_ID and OUTPUT_SQL below
 *   3. node scripts/generate-links-sql.js
 *   4. npx wrangler d1 execute akademo-db --remote --yes --file=docs/onboarding/{client}/files/import-links.sql
 *
 * Prerequisite: import-documents.sql must have run first so that Lesson rows exist.
 *
 * CSV columns expected (from 06_urls.sql):
 *   link_title, url, course_name, section_number, section_name, description
 */

const fs = require('fs');
const path = require('path');
const crypto = require('crypto');

// ─── CONFIGURE THESE ───────────────────────────────────────────────────────────
const ACADEMY_ID  = 'REPLACE_WITH_ACADEMY_ID';  // SELECT id FROM Academy WHERE name = '...'
const CLIENT_SLUG = 'maximo-expo';
const LINKS_CSV   = path.join(__dirname, '../links.csv');
const OUTPUT_SQL  = path.join(__dirname, `../docs/onboarding/${CLIENT_SLUG}/files/import-links.sql`);
// ───────────────────────────────────────────────────────────────────────────────

// ─── CSV PARSER ────────────────────────────────────────────────────────────────
function parseCSV(content) {
  const lines = content.trim().split(/\r?\n/);
  if (lines.length < 2) return [];

  const headers = splitCSVLine(lines[0]).map(h => h.replace(/^"|"$/g, '').trim());

  return lines.slice(1)
    .filter(l => l.trim())
    .map(line => {
      const values = splitCSVLine(line);
      const row = {};
      headers.forEach((h, i) => {
        row[h] = (values[i] || '').replace(/^"|"$/g, '').trim();
      });
      return row;
    });
}

function splitCSVLine(line) {
  const values = [];
  let current = '';
  let inQuotes = false;
  for (let i = 0; i < line.length; i++) {
    const ch = line[i];
    if (ch === '"') {
      // Handle escaped quotes ("")
      if (inQuotes && line[i + 1] === '"') { current += '"'; i++; }
      else { inQuotes = !inQuotes; }
    } else if (ch === ',' && !inQuotes) {
      values.push(current);
      current = '';
    } else {
      current += ch;
    }
  }
  values.push(current);
  return values;
}
// ───────────────────────────────────────────────────────────────────────────────

function esc(str) {
  return (str || '').replace(/'/g, "''");
}

// ─── MAIN ──────────────────────────────────────────────────────────────────────
if (!fs.existsSync(LINKS_CSV)) {
  console.error(`\nERROR: ${LINKS_CSV} not found.`);
  console.error('Run docs/onboarding/maximo-expo/queries/06_urls.sql in phpMyAdmin and save output as links.csv in the project root.\n');
  process.exit(1);
}

if (ACADEMY_ID === 'REPLACE_WITH_ACADEMY_ID') {
  console.error('\nERROR: Set ACADEMY_ID in scripts/generate-links-sql.js before running.\n');
  process.exit(1);
}

const rows = parseCSV(fs.readFileSync(LINKS_CSV, 'utf8'));
console.log(`Loaded ${rows.length} rows from links.csv`);

// Track orderIndex per (course_name + section_name) to support multiple links per topic
const orderCounters = new Map();
const sqlLines = [
  `-- import-links.sql`,
  `-- Auto-generated by scripts/generate-links-sql.js`,
  `-- Academy: ${ACADEMY_ID}`,
  `-- Run AFTER import-documents.sql (Lesson + Topic rows must already exist)`,
  `-- Total links: ${rows.length}`,
  ``,
];

let generated = 0;
let skipped = 0;
const skippedRows = [];

for (const row of rows) {
  const { link_title, url, course_name, section_name } = row;

  if (!link_title || !url || !course_name || !section_name) {
    skipped++;
    skippedRows.push(row);
    continue;
  }

  const key = `${course_name}||${section_name}`;
  const orderIndex = orderCounters.get(key) ?? 0;
  orderCounters.set(key, orderIndex + 1);

  const id = crypto.randomUUID();

  sqlLines.push(`-- ${course_name} / ${section_name}: ${link_title}`);
  sqlLines.push(`INSERT OR IGNORE INTO LessonLink (id, lessonId, title, url, orderIndex)`);
  sqlLines.push(`SELECT '${id}', l.id, '${esc(link_title)}', '${esc(url)}', ${orderIndex}`);
  sqlLines.push(`FROM Lesson l`);
  sqlLines.push(`JOIN Topic t ON t.id = l.topicId`);
  sqlLines.push(`JOIN Class c ON c.id = t.classId`);
  sqlLines.push(`WHERE c.academyId = '${ACADEMY_ID}'`);
  sqlLines.push(`  AND c.name = '${esc(course_name)}'`);
  sqlLines.push(`  AND t.name = '${esc(section_name)}'`);
  sqlLines.push(`LIMIT 1;`);
  sqlLines.push(``);
  generated++;
}

// Append a verification query at the bottom as a comment
sqlLines.push(`-- Verify: run this after import to check count`);
sqlLines.push(`-- SELECT COUNT(*) FROM LessonLink ll`);
sqlLines.push(`-- JOIN Lesson le ON le.id = ll.lessonId`);
sqlLines.push(`-- JOIN Topic t ON t.id = le.topicId`);
sqlLines.push(`-- JOIN Class c ON c.id = t.classId`);
sqlLines.push(`-- WHERE c.academyId = '${ACADEMY_ID}';`);

const output = sqlLines.join('\n');

// Ensure output directory exists
fs.mkdirSync(path.dirname(OUTPUT_SQL), { recursive: true });
fs.writeFileSync(OUTPUT_SQL, output, 'utf8');

console.log(`\n✓ Written: ${OUTPUT_SQL}`);
console.log(`  Generated: ${generated} INSERT statements`);
if (skipped > 0) {
  console.log(`  Skipped:   ${skipped} rows with missing fields`);
  skippedRows.forEach(r => console.log(`    - ${JSON.stringify(r)}`));
}

// Print unique course/section combos for sanity check
const combos = [...orderCounters.keys()].sort();
console.log(`\n  Unique course+section combos: ${combos.length}`);
combos.forEach(k => {
  const [course, section] = k.split('||');
  const count = orderCounters.get(k);
  console.log(`    [${count} link${count > 1 ? 's' : ''}] ${course} / ${section}`);
});

console.log(`\nNext: npx wrangler d1 execute akademo-db --remote --yes --file="${OUTPUT_SQL.replace(/\\/g, '/')}"`);
