const fs = require('fs');
const path = require('path');
const crypto = require('crypto');

const ACADEMY_ID  = '93ab97cf-271b-48de-924b-10fb7eab0a38';
const CLIENT_SLUG = 'maximo-expo';
const LINKS_CSV   = path.join(__dirname, '..', 'docs', 'onboarding', CLIENT_SLUG, 'files', 'links.csv');
const OUTPUT_SQL  = path.join(__dirname, '..', 'docs', 'onboarding', CLIENT_SLUG, 'files', 'import-links.sql');

function parseCSV(content) {
  const lines = content.trim().split(/\r?\n/);
  if (lines.length < 2) return [];
  const headers = splitCSVLine(lines[0]).map(h => h.replace(/^"|"$/g, '').trim());
  return lines.slice(1).filter(l => l.trim()).map(line => {
    const values = splitCSVLine(line);
    const row = {};
    headers.forEach((h, i) => { row[h] = (values[i] || '').replace(/^"|"$/g, '').trim(); });
    return row;
  });
}

function splitCSVLine(line) {
  const values = [];
  let current = '';
  let inQuotes = false;
  for (let i = 0; i < line.length; i++) {
    const ch = line[i];
    if (ch === '"') {
      if (inQuotes && line[i + 1] === '"') { current += '"'; i++; }
      else { inQuotes = !inQuotes; }
    } else if (ch === ',' && !inQuotes) { values.push(current); current = ''; }
    else { current += ch; }
  }
  values.push(current);
  return values;
}

function esc(s) { return (s || '').replace(/'/g, "''"); }

if (!fs.existsSync(LINKS_CSV)) { console.error('ERROR: ' + LINKS_CSV + ' not found.'); process.exit(1); }

const rows = parseCSV(fs.readFileSync(LINKS_CSV, 'utf8'));
console.log('Loaded ' + rows.length + ' rows from links.csv');

const now = new Date().toISOString();
const sqlLines = [
  '-- import-links.sql',
  '-- Auto-generated by scripts/generate-links-sql.js',
  '-- Each link is its own Lesson + LessonLink (mirrors API import-moodle behavior)',
  '-- Academy: ' + ACADEMY_ID,
  '',
  '-- Remove previously imported LessonLink rows for this academy (clean re-import)',
  'DELETE FROM LessonLink WHERE lessonId IN (',
  '  SELECT l.id FROM Lesson l',
  '  JOIN Topic t ON t.id = l.topicId',
  '  JOIN Class c ON c.id = t.classId',
  "  WHERE c.academyId = '" + ACADEMY_ID + "'",
  ');',
  '',
];

let generated = 0; let skipped = 0;
const orderCounters = new Map();

for (const row of rows) {
  const lt = row.link_title; const url = row.url;
  const cn = row.course_name; const sn = row.section_name;
  if (!lt || !url || !cn || !sn) { skipped++; continue; }

  const lessonId = crypto.randomUUID();
  const linkId   = crypto.randomUUID();
  const key = cn + '||' + sn;
  const oi  = orderCounters.get(key) || 0;
  orderCounters.set(key, oi + 1);

  sqlLines.push('-- ' + cn + ' / ' + sn + ': ' + lt);
  sqlLines.push("INSERT OR IGNORE INTO Lesson (id, title, classId, topicId, releaseDate, createdAt)");
  sqlLines.push("SELECT '" + lessonId + "', '" + esc(lt) + "', c.id, t.id, '" + now + "', '" + now + "'");
  sqlLines.push("FROM Topic t JOIN Class c ON c.id = t.classId");
  sqlLines.push("WHERE c.academyId = '" + ACADEMY_ID + "'");
  sqlLines.push("  AND LOWER(TRIM(c.name)) = LOWER(TRIM('" + esc(cn) + "'))");
  sqlLines.push("  AND LOWER(TRIM(t.name)) = LOWER(TRIM('" + esc(sn) + "'))");
  sqlLines.push("LIMIT 1;");
  sqlLines.push("INSERT OR IGNORE INTO LessonLink (id, lessonId, title, url, orderIndex, createdAt)");
  sqlLines.push("SELECT '" + linkId + "', l.id, '" + esc(lt) + "', '" + esc(url) + "', " + oi + ", '" + now + "'");
  sqlLines.push("FROM Lesson l JOIN Topic t ON t.id = l.topicId JOIN Class c ON c.id = t.classId");
  sqlLines.push("WHERE c.academyId = '" + ACADEMY_ID + "'");
  sqlLines.push("  AND LOWER(TRIM(c.name)) = LOWER(TRIM('" + esc(cn) + "'))");
  sqlLines.push("  AND LOWER(TRIM(t.name)) = LOWER(TRIM('" + esc(sn) + "'))");
  sqlLines.push("  AND l.title = '" + esc(lt) + "'");
  sqlLines.push("LIMIT 1;");
  sqlLines.push('');
  generated++;
}

fs.mkdirSync(path.dirname(OUTPUT_SQL), { recursive: true });
fs.writeFileSync(OUTPUT_SQL, sqlLines.join('\n'), 'utf8');
console.log('Written: ' + OUTPUT_SQL);
console.log('Generated: ' + generated + ' link entries (Lesson + LessonLink per link)');
if (skipped) console.log('Skipped: ' + skipped);
const combos = [...orderCounters.keys()].sort();
console.log('Unique course+section combos: ' + combos.length);
combos.forEach(k => { const p=k.split('||'); console.log('  [' + orderCounters.get(k) + '] ' + p[0] + ' / ' + p[1]); });