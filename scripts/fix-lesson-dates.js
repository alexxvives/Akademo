#!/usr/bin/env node
/**
 * fix-lesson-dates.js
 *
 * One-off script: reads files.csv and generates SQL UPDATE statements to
 * fix Lesson.releaseDate for imported lessons in Máximo Exponente (or any client).
 *
 * The script matches each lesson title (case-insensitive) against the file_title
 * column in files.csv and sets releaseDate to the original Moodle upload timestamp.
 *
 * Output: scripts/fix-lesson-dates.sql  — review, then run with:
 *   npx wrangler d1 execute akademo-db --remote --file scripts/fix-lesson-dates.sql
 *
 * Usage:
 *   node scripts/fix-lesson-dates.js [--client maximo-expo]
 */

const fs   = require('fs');
const path = require('path');

const CLIENT_SLUG = process.argv.includes('--client')
  ? process.argv[process.argv.indexOf('--client') + 1]
  : 'maximo-expo';

const ACADEMY_ID = '93ab97cf-271b-48de-924b-10fb7eab0a38';  // Máximo Exponente

const FILES_CSV = path.join(__dirname, '../docs/onboarding', CLIENT_SLUG, 'files', 'files.csv');
const OUT_SQL   = path.join(__dirname, 'fix-lesson-dates.sql');

// ── Minimal CSV parser (handles quoted fields) ──────────────────────────────
function parseCsv(text) {
  const lines = text.replace(/\r\n/g, '\n').replace(/\r/g, '\n').split('\n');
  return lines
    .filter(l => l.trim())
    .map(line => {
      const fields = [];
      let field = '';
      let inQuote = false;
      for (let i = 0; i < line.length; i++) {
        const ch = line[i];
        if (ch === '"') {
          if (inQuote && line[i + 1] === '"') { field += '"'; i++; }
          else inQuote = !inQuote;
        } else if (ch === ',' && !inQuote) {
          fields.push(field); field = '';
        } else {
          field += ch;
        }
      }
      fields.push(field);
      return fields;
    });
}

// ── Re-encode Moodle's Latin-1-mis-read-as-UTF-8 strings ────────────────────
function fixEncoding(s) {
  try {
    const bytes = Buffer.from(s.split('').map(c => c.charCodeAt(0) & 0xFF));
    const decoded = bytes.toString('utf8');
    // If it round-trips without replacement chars and shrinks/stays same length, use it
    if (!decoded.includes('\uFFFD') && decoded.length <= s.length) return decoded;
    return s;
  } catch { return s; }
}

function normalize(s) {
  return fixEncoding(String(s || '').trim()).toLowerCase();
}

function escSql(s) {
  return s.replace(/'/g, "''");
}

// ── Main ────────────────────────────────────────────────────────────────────
if (!fs.existsSync(FILES_CSV)) {
  console.error(`files.csv not found: ${FILES_CSV}`);
  process.exit(1);
}

const raw  = fs.readFileSync(FILES_CSV, 'utf-8');
const rows = parseCsv(raw);
const [header, ...dataRows] = rows;

const idx = name => header.indexOf(name);
const cols = {
  fileTitle:     idx('file_title'),
  fileTimestamp: idx('file_timestamp'),
  filename:      idx('filename'),
};

if (cols.fileTitle === -1 || cols.fileTimestamp === -1) {
  console.error('Missing expected columns in files.csv');
  process.exit(1);
}

// Build title → max ISO date map (keep most recent timestamp per title)
const titleToDate = new Map();
for (const row of dataRows) {
  const rawTitle = row[cols.fileTitle];
  const ts       = parseInt(row[cols.fileTimestamp], 10);
  if (!rawTitle || !ts) continue;

  const key = normalize(rawTitle);
  const iso = new Date(ts * 1000).toISOString();
  if (!titleToDate.has(key) || iso > titleToDate.get(key)) {
    titleToDate.set(key, iso);
  }
}
console.log(`Built timestamp map for ${titleToDate.size} unique titles from files.csv`);

// Generate SQL
const lines = [
  `-- fix-lesson-dates.sql`,
  `-- Generated by scripts/fix-lesson-dates.js`,
  `-- Updates Lesson.releaseDate to the original Moodle upload date`,
  `-- for all lessons in academy ${ACADEMY_ID} whose title matches files.csv`,
  `-- Run with: npx wrangler d1 execute akademo-db --remote --file scripts/fix-lesson-dates.sql`,
  ``,
];

let matchCount = 0;
for (const [normTitle, isoDate] of titleToDate.entries()) {
  lines.push(
    `UPDATE Lesson SET releaseDate = '${isoDate}'` +
    ` WHERE classId IN (SELECT id FROM Class WHERE academyId = '${ACADEMY_ID}')` +
    ` AND LOWER(title) = '${escSql(normTitle)}';`
  );
  matchCount++;
}

// Also fix Topic.createdAt for topics that were created during import (optional bonus)
lines.push('');
lines.push('-- Done');

const sql = lines.join('\n');
fs.writeFileSync(OUT_SQL, sql, 'utf-8');

console.log(`Wrote ${matchCount} UPDATE statements to ${OUT_SQL}`);
console.log('Review the file, then run:');
console.log('  npx wrangler d1 execute akademo-db --remote --file scripts/fix-lesson-dates.sql');
