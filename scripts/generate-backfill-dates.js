/**
 * generate-backfill-dates.js
 *
 * Reads files.csv + documents-manifest.json and produces a SQL file that
 * back-fills the original Moodle upload timestamps onto already-imported
 * Lesson rows for Maximo Exponente.
 *
 * Strategy:
 *   1. Build map: filePath -> maxTimestamp (Unix seconds) from files.csv
 *   2. Load documents-manifest.json (fileTitle/courseName/sectionName = exact D1 values)
 *   3. For each manifest entry, strip R2 prefix from r2Key to get filePath,
 *      look up the timestamp, and generate an UPDATE.
 *
 * Output: docs/onboarding/maximo-expo/files/backfill-lesson-dates.sql
 *
 * Run:
 *   node scripts/generate-backfill-dates.js
 *   $env:CLOUDFLARE_ACCOUNT_ID="53808754f4bd94365d6e31351aa426c0"
 *   npx wrangler d1 execute akademo-db --remote --yes --file=docs/onboarding/maximo-expo/files/backfill-lesson-dates.sql
 */

const fs   = require('fs');
const path = require('path');

const CSV_PATH      = path.join(__dirname, '../docs/onboarding/maximo-expo/files/files.csv');
const MANIFEST_PATH = path.join(__dirname, '../docs/onboarding/maximo-expo/files/documents-manifest.json');
const OUT_PATH      = path.join(__dirname, '../docs/onboarding/maximo-expo/files/backfill-lesson-dates.sql');
const ACADEMY_ID    = '93ab97cf-271b-48de-924b-10fb7eab0a38';
const R2_PREFIX     = 'maximo-expo';

function parseCSVLine(line) {
  const fields = [];
  let current = '';
  let inQuotes = false;
  for (let i = 0; i < line.length; i++) {
    const ch = line[i];
    if (ch === '"') {
      if (inQuotes && line[i + 1] === '"') { current += '"'; i++; }
      else { inQuotes = !inQuotes; }
    } else if (ch === ',' && !inQuotes) {
      fields.push(current);
      current = '';
    } else {
      current += ch;
    }
  }
  fields.push(current);
  return fields;
}

function sq(str) { return str.replace(/'/g, "''"); }

// Step 1: Build filePath -> maxTimestamp map from files.csv (read as latin1)
const rawCSV   = fs.readFileSync(CSV_PATH);
const csvText  = rawCSV.toString('latin1');
const csvLines = csvText.split('\n').map(l => l.trim()).filter(Boolean);
const header   = parseCSVLine(csvLines[0]);

const COL = {
  filePath:      header.indexOf('file_path'),
  fileTimestamp: header.indexOf('file_timestamp'),
};

if (COL.filePath === -1 || COL.fileTimestamp === -1) {
  console.error('Required columns not found in CSV'); process.exit(1);
}

const filePathToTs = new Map();
for (let i = 1; i < csvLines.length; i++) {
  const row = parseCSVLine(csvLines[i]);
  const fp  = (row[COL.filePath]      || '').trim();
  const ts  = parseInt(row[COL.fileTimestamp], 10);
  if (!fp || !ts) continue;
  const prev = filePathToTs.get(fp) || 0;
  if (ts > prev) filePathToTs.set(fp, ts);
}
console.log(`Loaded ${filePathToTs.size} file->timestamp mappings from CSV`);

// Step 2: Load manifest and generate SQL
const manifest   = JSON.parse(fs.readFileSync(MANIFEST_PATH, 'utf8'));
const statements = [
  '-- backfill-lesson-dates.sql',
  '-- Auto-generated by scripts/generate-backfill-dates.js (manifest-based)',
  `-- Maximo Exponente academyId = ${ACADEMY_ID}`,
  '',
];

let count = 0;
let noTs  = 0;
const seen = new Set();

for (const entry of manifest) {
  const { fileTitle, courseName, sectionName, r2Key } = entry;
  if (!fileTitle || !courseName || !r2Key) continue;

  const filePath = r2Key.startsWith(R2_PREFIX + '/') ? r2Key.slice(R2_PREFIX.length + 1) : r2Key;
  const ts = filePathToTs.get(filePath) || 0;
  if (!ts) { noTs++; continue; }

  const dedupeKey = `${courseName}|||${sectionName || ''}|||${fileTitle}`;
  if (seen.has(dedupeKey)) continue;
  seen.add(dedupeKey);

  const isoDate = new Date(ts * 1000).toISOString();
  const secName = (sectionName || 'Documentos').trim();

  statements.push(
    `UPDATE Lesson SET createdAt = '${isoDate}', releaseDate = '${isoDate}' ` +
    `WHERE title = '${sq(fileTitle)}' ` +
    `AND topicId IN (` +
      `SELECT t.id FROM Topic t ` +
      `JOIN Class c ON c.id = t.classId ` +
      `WHERE c.academyId = '${ACADEMY_ID}' ` +
      `AND LOWER(TRIM(c.name)) = LOWER(TRIM('${sq(courseName)}')) ` +
      `AND LOWER(TRIM(t.name)) = LOWER(TRIM('${sq(secName)}'))` +
    `);`
  );
  count++;
}

statements.push('');
statements.push(`-- Total statements: ${count} (skipped ${noTs} entries without timestamp)`);

fs.writeFileSync(OUT_PATH, statements.join('\n'));
console.log(`Wrote ${count} UPDATE statements -> ${OUT_PATH}`);
if (noTs) console.log(`  (${noTs} manifest entries had no matching timestamp in CSV)`);
