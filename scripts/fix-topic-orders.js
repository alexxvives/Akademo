#!/usr/bin/env node
/**
 * fix-topic-orders.js
 *
 * One-off script: reads documents-manifest.json and generates SQL UPDATE
 * statements to set Topic.orderIndex = sectionNumber from Moodle for all
 * topics in Máximo Exponente.
 *
 * Output: scripts/fix-topic-orders.sql
 * Apply:  npx wrangler d1 execute akademo-db --remote --file scripts/fix-topic-orders.sql
 *
 * Usage:
 *   node scripts/fix-topic-orders.js [--client maximo-expo]
 */

const fs   = require('fs');
const path = require('path');

const CLIENT_SLUG = process.argv.includes('--client')
  ? process.argv[process.argv.indexOf('--client') + 1]
  : 'maximo-expo';

const ACADEMY_ID = '93ab97cf-271b-48de-924b-10fb7eab0a38';

const MANIFEST = path.join(__dirname, '../docs/onboarding', CLIENT_SLUG, 'files', 'documents-manifest.json');
const OUT_SQL  = path.join(__dirname, 'fix-topic-orders.sql');

if (!fs.existsSync(MANIFEST)) {
  console.error(`Manifest not found: ${MANIFEST}`);
  process.exit(1);
}

const manifest = JSON.parse(fs.readFileSync(MANIFEST, 'utf-8'));

// Re-encode Moodle's Latin-1-mis-read-as-UTF-8 strings (same as API's fixEncoding)
function fixEncoding(s) {
  try {
    const bytes = Buffer.from(s.split('').map(c => c.charCodeAt(0) & 0xFF));
    const decoded = bytes.toString('utf8');
    if (!decoded.includes('\uFFFD') && decoded.length <= s.length) return decoded;
    return s;
  } catch { return s; }
}

// Build unique (courseName, sectionName) → min sectionNumber map
// Use the minimum sectionNumber for each section name within a course.
const key = (course, section) => `${course.toLowerCase().trim()}|||${section.toLowerCase().trim()}`;
const orderMap = new Map(); // key → { courseName, sectionName, sectionNumber }

for (const entry of manifest) {
  const course  = fixEncoding(String(entry.courseName  || '').trim());
  const section = fixEncoding(String(entry.sectionName || '').trim());
  const num     = typeof entry.sectionNumber === 'number' ? entry.sectionNumber : null;
  if (!course || !section || num === null) continue;
  const k = key(course, section);
  if (!orderMap.has(k) || orderMap.get(k).sectionNumber > num) {
    orderMap.set(k, { courseName: course, sectionName: section, sectionNumber: num });
  }
}

console.log(`Found ${orderMap.size} unique (course, section) pairs`);

const escSql = s => s.replace(/'/g, "''");

const lines = [
  `-- fix-topic-orders.sql`,
  `-- Generated by scripts/fix-topic-orders.js`,
  `-- Sets Topic.orderIndex = Moodle sectionNumber for academy ${ACADEMY_ID}`,
  `-- Run: npx wrangler d1 execute akademo-db --remote --file scripts/fix-topic-orders.sql`,
  ``,
];

for (const { courseName, sectionName, sectionNumber } of orderMap.values()) {
  lines.push(
    `UPDATE Topic SET orderIndex = ${sectionNumber}` +
    ` WHERE classId IN (SELECT id FROM Class WHERE academyId = '${ACADEMY_ID}' AND LOWER(name) = '${escSql(courseName.toLowerCase())}')` +
    ` AND LOWER(name) = '${escSql(sectionName.toLowerCase())}';`
  );
}

lines.push('');
lines.push('-- Done');

fs.writeFileSync(OUT_SQL, lines.join('\n'), 'utf-8');
console.log(`Wrote ${orderMap.size} UPDATE statements to ${OUT_SQL}`);
console.log('Apply with:');
console.log('  npx wrangler d1 execute akademo-db --remote --file scripts/fix-topic-orders.sql');
